package in.vikash.dto;

import java.io.Serializable;

public class Faculty implements Serializable{
	Integer fac_id ;
    String fac_name ;
    String fac_fathers_name;
    String fac_gender ;
    String fac_mobile ;
    String fac_email ;
    String fac_qualification ;
    String fac_address ;
    String fac_password ;
	public Integer getFac_id() {
		return fac_id;
	}
	public void setFac_id(Integer fac_id) {
		this.fac_id = fac_id;
	}
	public String getFac_name() {
		return fac_name;
	}
	public void setFac_name(String fac_name) {
		this.fac_name = fac_name;
	}
	public String getFac_fathers_name() {
		return fac_fathers_name;
	}
	public void setFac_fathers_name(String fac_fathers_name) {
		this.fac_fathers_name = fac_fathers_name;
	}
	public String getFac_gender() {
		return fac_gender;
	}
	public void setFac_gender(String fac_gender) {
		this.fac_gender = fac_gender;
	}
	public String getFac_mobile() {
		return fac_mobile;
	}
	public void setFac_mobile(String fac_mobile) {
		this.fac_mobile = fac_mobile;
	}
	public String getFac_email() {
		return fac_email;
	}
	public void setFac_email(String fac_email) {
		this.fac_email = fac_email;
	}
	public String getFac_qualification() {
		return fac_qualification;
	}
	public void setFac_qualification(String fac_qualification) {
		this.fac_qualification = fac_qualification;
	}
	public String getFac_address() {
		return fac_address;
	}
	public void setFac_address(String fac_address) {
		this.fac_address = fac_address;
	}
	public String getFac_password() {
		return fac_password;
	}
	public void setFac_password(String fac_password) {
		this.fac_password = fac_password;
	}
    
    
}
